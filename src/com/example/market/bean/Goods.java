package com.example.market.bean;

public class Goods {

	private String promotePrice;// 促销价

	private String id;// 自增id

	private String name;// 商品名称

	private String brief;// 简短描述

	private String brandName;// 商品所属品牌名称

	private String goodsStyleName;// 商品所属类型名称

	private String shortName;// 缩略名称

	private String shortStyleName;// 产品缩短名

	private String marketPrice;// 市场售价

	private String shopPrice;// 市场售价

	private String thumb;// 商品小图片地址

	private String goodsImg;

	private String url;

	private String conUrl;// 商品详情地址

	public String getConUrl() {
		return conUrl;
	}

	public void setConUrl(String conUrl) {
		this.conUrl = conUrl;
	}

	public String getPromotePrice() {
		return promotePrice;
	}

	public void setPromotePrice(String promotePrice) {
		this.promotePrice = promotePrice;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBrief() {
		return brief;
	}

	public void setBrief(String brief) {
		this.brief = brief;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getGoodsStyleName() {
		return goodsStyleName;
	}

	public void setGoodsStyleName(String goodsStyleName) {
		this.goodsStyleName = goodsStyleName;
	}

	public String getShortName() {
		return shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}

	public String getShortStyleName() {
		return shortStyleName;
	}

	public void setShortStyleName(String shortStyleName) {
		this.shortStyleName = shortStyleName;
	}

	public String getMarketPrice() {
		return marketPrice;
	}

	public void setMarketPrice(String marketPrice) {
		this.marketPrice = marketPrice;
	}

	public String getShopPrice() {
		return shopPrice;
	}

	public void setShopPrice(String shopPrice) {
		this.shopPrice = shopPrice;
	}

	public String getThumb() {
		return thumb;
	}

	public void setThumb(String thumb) {
		this.thumb = thumb;
	}

	public String getGoodsImg() {
		return goodsImg;
	}

	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
